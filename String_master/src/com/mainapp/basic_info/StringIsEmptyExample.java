package com.mainapp.basic_info;

public class StringIsEmptyExample {
    public static void main(String[] args) {
        String str1 = "";
        String str2 = "Hello";

        System.out.println("Is str1 empty? " + str1.isEmpty()); // true
        System.out.println("Is str2 empty? " + str2.isEmpty()); // false
    }
}
