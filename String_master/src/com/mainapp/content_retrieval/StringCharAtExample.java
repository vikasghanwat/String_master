package com.mainapp.content_retrieval;

public class StringCharAtExample {
    public static void main(String[] args) {
        String name = "Vikas";

        System.out.println("Character at index 0: " + name.charAt(0)); // 'V'
        System.out.println("Character at index 3: " + name.charAt(3)); // 'a'
    }
}

